package com.phly.ttw.manage.supplier.util;

import org.apache.commons.lang.RandomStringUtils;


/***
 * 供应商编号产生类
 * 
 * @author Administrator
 * 
 */
public class SupplierNoUtil {

	private static final String company = "C";// 公司

	private static final String person = "P";// 公司

	/***
	 * 获取个人版
	 * 
	 * @return
	 */
	public static String getSupplier_P() {
		return person + RandomStringUtils.randomNumeric(6);
	}

	/***
	 * 公司版
	 * 
	 * @return
	 */
	public static String getSupplier_C() {
		return company + RandomStringUtils.randomNumeric(6);
	}
}
